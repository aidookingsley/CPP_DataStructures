#include <iostream>
#include <string>
using namespace std;

int main()
{
    int op1 = 3, op2 = 4;
    float op3 = 10.1, op4 = 5.4;
    cout << "Operands are op1 = " << op1 << " op2 = ";
    cout << " op3 = " << op3 << " op4 = " << op4;
    cout << endl;
    cout << "op1 + op2 = " << op1 + op2 << endl;
    cout << "op1 - op2 = " << op1 - op2 << endl;
    cout << "op3 * op4 = " << op3 * op4 << endl;
    cout << "op3 / op4 = " << op3 / op4 << endl;
    cout << "op2 % op1 = " << op2 % op1 << endl;
}